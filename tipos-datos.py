
# string
# cadena de texto o caracteres
# se declara con comillas
my_name ="Pedro"

# int
# tipo de dato tipo numero o enteros
# se declara sin comillas
my_age = 32

# boolean
# solo son valores: true o false
is_single = True

# inputs
# funcion que permite el ingreso de datos del usuario
# Los valores ingresados siempre seran tratados como tipo de dato string
my_age =input("Cual es tu edad?: ")


# FUNCION type()
# imprime el tipo de dato
print("Mi nombre es",my_name,"y es de tipo:",type(my_name))
print("Mi edad es",my_age,"y es de tipo",type(my_age))
print("Estoy soltero",is_single,"y es de tipo",type(is_single))
