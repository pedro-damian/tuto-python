
# CARACTERES 
# Las computadoras almacenan los caracteres como números.

# ASCII (American Standard Code for Information Interchange) (El Código Estándar Americano para Intercambio de Información)
# es el más utilizado, y es posible suponer que casi todos los dispositivos modernos (como computadoras, impresoras, teléfonos móviles, tabletas, etc.)
# El código proporciona espacio para 256 caracteres diferentes
# El código ASCII emplea ocho bits para cada signo


# |18N

# UNICODE
# Unicode asigna caracteres únicos (letras, guiones, ideogramas, etc.) a más de un millón de puntos de código
# capaz de codificar prácticamente todos los alfabetos que utilizan los seres humanos

# UCS-4 (Universal Character Set (Conjunto de Caracteres Universales).)
# UCS-4 emplea 32 bits (cuatro bytes) para almacenar cada carácter


# UTF-8 (Unicode Transformation Format (Formato de Transformación Unicode))
# Es el mas utilizado ya que desperdicia menos espacio de memoria
# UTF-8 emplea tantos bits para cada uno de los puntos de código como realmente necesita para representarlos.


# ¿Que es BOM?
# Una Marca de Orden de Bytes es una combinación especial de bits que anuncia la codificación utilizada por el contenido de un archivo (por ejemplo, UCS-4 o UTF-B).

# ¿Que es Punto de Codigo?
# Un número correspondiente a un carácter en particular