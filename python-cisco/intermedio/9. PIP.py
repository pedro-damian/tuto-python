
#>>>>>>>>>>>>>>>>>>>>>>>>>>>> Cómo usar pip <<<<<<<<<<<<<<<<<<<<<<<<<<
# Para saber que operaciones se puede realizar con pip escribe el siguiente comando en la terminal: pip help
# Para saber que funcion realiza cada operacion : pip help nombre_operacion
# Si deseas saber qué paquetes de Python se han instalado hasta ahora: pip list
# muestra informacion mas detallada sobre los paquetes instalados: pip show nombre_paquete
# si deseas encontrar el paquete deseado: pip search nombre_paquete
# o tambien puedes utilizar el sitio web: https://pypi.org/search

# >>>>>>>> Instalar un paquete con PIP  <<<<<<<<<<<<<<<<<<<<<<<<
# Hay 2 formas de instalar un paquete con privilegios y sin privilegios de administrador
# Sin privilegios: pip install --user pygame 
# Con privilegios: pip install pygame 

#>>>>>>>>>>>> Instalar una versión seleccionada de un paquete con PIP <<<<<<<<<<<<<<<<<<
# pip install nombre_del_paquete==versión_del_paquete || pip install pygame==1.9.2

#>>>>>>>>> Desinstalar un paquete con PIP  <<<<<<<<<<<<<<<<<<<<<<<
# pip uninstall nombre_del_paquete  || pip uninstall pygame

