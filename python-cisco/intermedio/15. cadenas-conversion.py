
# ------- ¿Cómo convertir un número (un entero o un flotante) en una cadena, y viceversa? ----------

#>>>>>>>>>>>> La conversión de numero a cadena - str()

entero = 13
flotante = 1.3
cadena_entero = str(entero)
cadena_flotante = str(flotante)

print(cadena_entero + ' ' + cadena_flotante)    # 13 1.3


