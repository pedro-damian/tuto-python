
# DICCIONARIOS
# es mutable.
# un diccionario es un conjunto de pares de claves y valores
# Cada clave debe de ser única.
# Una clave puede ser un tipo de dato de cualquier tipo.
# diccionario almacena pares de valores.
# La lista de todos los pares es encerrada con llaves, mientras que los pares son separados por comas, y las claves y valores por dos puntos.

dictionary = {"gato" : "chat", "perro" : "chien", "caballo" : "cheval"}
phone_numbers = {'jefe': 5551234567, 'Suzy': 22657854310}
empty_dictionary = {}

print(dictionary)
print(phone_numbers)
print(empty_dictionary)

