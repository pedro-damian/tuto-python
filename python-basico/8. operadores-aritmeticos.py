a = 5
b = 2
c= 10

# SUMA
print("La suma de a y b es", a+b)

# RESTA
print("La resta de a y b es", a-b)

# MULTIPLICAR
print("La multiplicacion de a y b es", a*b)

# DIVISION
# Esta division muestra el resultado con decimales
print("La division de a y b es", a/b)

# Esta division muestra el resultado de un numero entero sin decimales
print("La division de a y b es", a//b)

# MODULO
# mostrara el residuo de una division exacta o inexacta
print("La division de a y b es inexacta y su residuo es ", a%b)
print("La division de a y b es exacta y su residuo es ", c%a)

# POTENCIACION
print("La potencia de a elevado en b es: ",a**b)